        WHEN 'CA'                            THEN 'CIA_EMITIDO'
        WHEN 'AGUARDANDO_PAGAMENTO'          THEN 'CIA_CIENCIA'
        WHEN 'AGUARDANDO_PAGAMENTO_VISTORIA' THEN 'CIA_CIENCIA'
