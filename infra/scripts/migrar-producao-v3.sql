-- ============================================================
-- PASSO 2 — USUARIO
--   Deduplicação por email: mantém o registro mais antigo
--   StatusCadastro: 0=INCOMPLETO 1=ANALISE_PENDENTE 2=EM_ANALISE
--                   3=APROVADO   4=REPROVADO
-- ============================================================
PROMPT === PASSO 2: Migrando USUARIO ===
INSERT INTO SOL.USUARIO (
    ID_USUARIO, CPF, NOME, EMAIL, TELEFONE,
    TIPO_USUARIO, STATUS_CADASTRO, ATIVO,
    DT_CRIACAO, DT_ATUALIZACAO
)
SELECT NRO_INT_USUARIO, CPF, NOME, EMAIL, TELEFONE,
       TIPO_USUARIO, STATUS_CADASTRO, ATIVO,
       DT_CRIACAO, DT_ATUALIZACAO
FROM (
    SELECT
        u.NRO_INT_USUARIO,
        SUBSTR(REGEXP_REPLACE(u.TXT_CPF, '[^0-9]', ''), 1, 11)   AS CPF,
        SUBSTR(u.NOME_USUARIO, 1, 200)                            AS NOME,
        LOWER(TRIM(u.TXT_EMAIL))                                  AS EMAIL,
        SUBSTR(u.TXT_TELEFONE1, 1, 20)                            AS TELEFONE,
        CASE
            WHEN EXISTS (SELECT 1 FROM CBM_RESPONSAVEL_TECNICO rt
                         WHERE rt.NRO_INT_USUARIO = u.NRO_INT_USUARIO AND ROWNUM = 1)
            THEN 'RT' ELSE 'CIDADAO'
        END                                                       AS TIPO_USUARIO,
        CASE u.TP_STATUS
            WHEN 0 THEN 'INCOMPLETO'
            WHEN 1 THEN 'ANALISE_PENDENTE'
            WHEN 2 THEN 'EM_ANALISE'
            WHEN 3 THEN 'APROVADO'
            WHEN 4 THEN 'REPROVADO'
            ELSE        'INCOMPLETO'
        END                                                       AS STATUS_CADASTRO,
        'S'                                                       AS ATIVO,
        CAST(COALESCE(u.CTR_DTH_INC, SYSTIMESTAMP) AS DATE)      AS DT_CRIACAO,
        CAST(COALESCE(u.CTR_DTH_ATU, SYSTIMESTAMP) AS DATE)      AS DT_ATUALIZACAO,
        ROW_NUMBER() OVER (
            PARTITION BY LOWER(TRIM(u.TXT_EMAIL))
            ORDER BY u.NRO_INT_USUARIO ASC
        ) AS RN
    FROM CBM_USUARIO u
    WHERE u.TXT_EMAIL IS NOT NULL
      AND TRIM(u.TXT_EMAIL) IS NOT NULL
      AND REGEXP_REPLACE(u.TXT_CPF, '[^0-9]', '') IS NOT NULL
)
WHERE RN = 1;

DECLARE v NUMBER; BEGIN SELECT COUNT(*) INTO v FROM SOL.USUARIO; DBMS_OUTPUT.PUT_LINE('USUARIO inserido: ' || v); END;
/

-- ============================================================
-- PASSO 3 — ENDERECO (CEP NULL → '00000000' como placeholder)
-- ============================================================
PROMPT === PASSO 3: Migrando ENDERECO ===
INSERT INTO SOL.ENDERECO (
    ID_ENDERECO, CEP, LOGRADOURO, NUMERO, COMPLEMENTO,
    BAIRRO, MUNICIPIO, UF, LATITUDE, LONGITUDE,
    DT_CRIACAO, DT_ATUALIZACAO
)
SELECT
    loc.NRO_INT_LOCALIZACAO,
    COALESCE(SUBSTR(REGEXP_REPLACE(loc.TXT_CEP, '[^0-9]', ''), 1, 8), '00000000'),
    SUBSTR(el.TXT_LOGRADOURO, 1, 200),
    SUBSTR(TO_CHAR(el.NRO_IMOVEL), 1, 20),
    SUBSTR(loc.TXT_COMPLEMENTO, 1, 100),
    SUBSTR(COALESCE(loc.TXT_BAIRRO, 'NAO INFORMADO'), 1, 100),
    SUBSTR(el.TXT_CIDADE, 1, 100),
    SUBSTR(el.SIGLA_UF, 1, 2),
    loc.NRO_LATITUDE_MAPA,
    loc.NRO_LONGITUDE_MAPA,
    CAST(COALESCE(loc.CTR_DTH_INC, SYSTIMESTAMP) AS DATE),
    CAST(COALESCE(loc.CTR_DTH_ATU, SYSTIMESTAMP) AS DATE)
FROM CBM_LICENCIAMENTO lic
JOIN CBM_LOCALIZACAO loc
    ON loc.NRO_INT_LOCALIZACAO = lic.NRO_INT_LOCALIZACAO
JOIN CBM_ENDERECO_LICENCIAMENTO el
    ON el.NRO_INT_ENDERECO_LICENCIAMENTO = loc.NRO_INT_ENDERECO_LICENCIAMENTO
WHERE lic.NRO_INT_LOCALIZACAO IS NOT NULL
  AND el.TXT_LOGRADOURO IS NOT NULL
  AND el.TXT_CIDADE IS NOT NULL
  AND el.SIGLA_UF IS NOT NULL;

