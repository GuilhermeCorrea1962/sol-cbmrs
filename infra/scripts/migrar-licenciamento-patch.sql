-- Diagnóstico: qual FK está falhando
SELECT c.constraint_name, c.r_constraint_name,
       cc.column_name, rc.table_name AS tabela_pai
FROM all_constraints c
JOIN all_cons_columns cc ON cc.constraint_name = c.constraint_name AND cc.owner = c.owner
JOIN all_constraints rc  ON rc.constraint_name = c.r_constraint_name
WHERE c.owner = 'SOL'
  AND c.table_name = 'LICENCIAMENTO'
  AND c.constraint_type = 'R'
ORDER BY c.constraint_name;

-- Quantos licenciamentos têm RT/RU cujo ID não está em SOL.USUARIO
DECLARE
  v_rt NUMBER; v_ru NUMBER;
BEGIN
  SELECT COUNT(*) INTO v_rt
  FROM CBM_LICENCIAMENTO lic
  WHERE EXISTS (
    SELECT 1 FROM CBM_RESPONSAVEL_TECNICO rt
    WHERE rt.NRO_INT_LICENCIAMENTO = lic.NRO_INT_LICENCIAMENTO
      AND rt.IND_ACEITE = 'S'
      AND NOT EXISTS (SELECT 1 FROM SOL.USUARIO u WHERE u.ID_USUARIO = rt.NRO_INT_USUARIO)
      AND ROWNUM = 1
  );
  SELECT COUNT(*) INTO v_ru
  FROM CBM_LICENCIAMENTO lic
  WHERE EXISTS (
    SELECT 1 FROM CBM_RESPONSAVEL_USO ru
    WHERE ru.NRO_INT_LICENCIAMENTO = lic.NRO_INT_LICENCIAMENTO
      AND ru.IND_ACEITE = 'S'
      AND NOT EXISTS (SELECT 1 FROM SOL.USUARIO u WHERE u.ID_USUARIO = ru.NRO_INT_USUARIO)
      AND ROWNUM = 1
  );
  DBMS_OUTPUT.PUT_LINE('Licenciamentos com RT fora do SOL.USUARIO: ' || v_rt);
  DBMS_OUTPUT.PUT_LINE('Licenciamentos com RU fora do SOL.USUARIO: ' || v_ru);
END;
