INSERT INTO SOL.LICENCIAMENTO (
    ID_LICENCIAMENTO, NUMERO_PPCI, TIPO, STATUS,
    ID_ENDERECO, ID_RESPONSAVEL_TECNICO, ID_RESPONSAVEL_USO,
