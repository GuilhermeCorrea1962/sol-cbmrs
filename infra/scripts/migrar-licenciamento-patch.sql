INSERT INTO SOL.ARQUIVO_ED (
    ID_ARQUIVO_ED, NOME_ARQUIVO, IDENTIFICADOR_ALFRESCO,
    TIPO_ARQUIVO, ID_LICENCIAMENTO, DT_UPLOAD
)
SELECT NRO_INT_ARQUIVO, NOME_ARQUIVO, IDENT_ALFRESCO, TIPO, ID_LIC, DT_UP
FROM (
    SELECT a.NRO_INT_ARQUIVO,
           SUBSTR(COALESCE(a.NOME_ARQUIVO,'sem-nome'),1,500) AS NOME_ARQUIVO,
           SUBSTR(a.TXT_IDENTIFICADOR_ALFRESCO,1,500) AS IDENT_ALFRESCO,
           'PRPCI' AS TIPO, p.NRO_INT_LICENCIAMENTO AS ID_LIC,
           CAST(COALESCE(a.CTR_DTH_INC,SYSTIMESTAMP) AS DATE) AS DT_UP
    FROM CBM_PRPCI p JOIN CBM_ARQUIVO a ON a.NRO_INT_ARQUIVO = p.NRO_INT_ARQUIVO
    WHERE EXISTS (SELECT 1 FROM SOL.LICENCIAMENTO l WHERE l.ID_LICENCIAMENTO = p.NRO_INT_LICENCIAMENTO)
    UNION ALL
    SELECT a.NRO_INT_ARQUIVO,
           SUBSTR(COALESCE(a.NOME_ARQUIVO,'sem-nome'),1,500),
           SUBSTR(a.TXT_IDENTIFICADOR_ALFRESCO,1,500),
           'APPCI', ap.NRO_INT_LICENCIAMENTO,
           CAST(COALESCE(a.CTR_DTH_INC,SYSTIMESTAMP) AS DATE)
    FROM CBM_APPCI ap JOIN CBM_ARQUIVO a ON a.NRO_INT_ARQUIVO = ap.NRO_INT_ARQUIVO
    WHERE EXISTS (SELECT 1 FROM SOL.LICENCIAMENTO l WHERE l.ID_LICENCIAMENTO = ap.NRO_INT_LICENCIAMENTO)
) src
WHERE NOT EXISTS (SELECT 1 FROM SOL.ARQUIVO_ED ae WHERE ae.ID_ARQUIVO_ED = src.NRO_INT_ARQUIVO);

